
public class OperationsPractice {

	public static void main(String[] args) 
	{
		int number; //Data type then variable name
		number = 20;
		System.out.println(number);
		number += 5;
		number /= 2;
		System.out.println(number);
		number *= 4;
		System.out.println(number);
		number -= 6;
		System.out.println(number);
		number %= 2;
		System.out.println(number);
	}

}
